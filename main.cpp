void ShowTime(unsigned long secondes)
{
    int ss = secondes % 60;
    int mn = (secondes / 60) % 60;
    int hh = (secondes / 3600);
    if( hh == 0 && mn == 0 ){
        printf("..h..'%2.2d", ss);
    }else if( hh == 0 ){
        printf("..h%2.2d'%2.2d", mn, ss);
    }else{
        printf("%2.2dh%2.2d'%2.2d", hh, mn, ss);
    }
}

const unsigned char preset  = 0x80;
const unsigned char nreset  = 0x00;
const unsigned char active  = 0x40;
const unsigned char disable = 0x00;

void board_start(int fileDescriptor, unsigned char snr)
{
  const unsigned char cmde = active | snr;
  int wBytes = write( fileDescriptor, &cmde, 1 );
  assert( wBytes == 1 );
  sleep( 1 );
}

void board_stop (int fileDescriptor, unsigned char snr)
{
  const unsigned char cmde = disable | snr;
  int wBytes = write( fileDescriptor, &cmde, 1 );
  assert( wBytes == 1 );
  sleep( 1 );
}

void board_reset(int fileDescriptor, unsigned char snr)
{
  const unsigned char cmd1 = preset | snr;
//  printf("Sending 0x%2.2X\n", cmd1);
  int w1Bytes = write( fileDescriptor, &cmd1, 1 );
  assert( w1Bytes == 1 );
  sleep( 1 );
  const unsigned char cmd2 = nreset | snr;
//  printf("Sending 0x%2.2X\n", cmd2);
  int w2Bytes = write( fileDescriptor, &cmd2, 1 );
  assert( w2Bytes == 1 );
  sleep( 1 );
}

